public class incremento{

        public static void main(String[] args){
            int c=5;
            System.out.printf("\nantes de incrementar.....%d",c);
            System.out.printf("\npos incremento...........%d",c++);
            System.out.printf("\ndepois de incrementar....%d",c);

            System.out.println("\n");

            c=5;
            System.out.printf("\nantes do pre incremento...%d",c);
            System.out.printf("\npre incremento............%d",++c);
            System.out.printf("\ndepois do pre incremento..%d%n",c);

        }
}